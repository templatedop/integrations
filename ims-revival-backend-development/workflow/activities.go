// CreateRevivalRequestActivity creates the revival request in database with workflow IDs
// This is the FIRST activity in the workflow-first pattern
// Idempotent: Safe to retry (uses ON CONFLICT on ticket_id)
// Returns: request_id (UUID generated by database)
// Note: MissingDocumentsList is NOT stored at indexing - sent by data entry/QC/approver only
func (a *Activities) CreateRevivalRequestActivity(ctx context.Context, input RevivalRequestInput) (string, error) {
	// Prepare document field (empty string if not provided)
	// Note: MissingDocumentsList not stored at indexing
	var docs *string
	if input.Documents != "" {
		docs = &input.Documents
	}

	// Map workflow input to domain model
	revivalReq := domain.RevivalRequest{
		TicketID:      input.TicketID,
		PolicyNumber:  input.PolicyNumber,
		RequestType:   input.RequestType,
		CurrentStatus: input.CurrentStatus,
		IndexedDate:   &input.IndexedDate,
		IndexedBy:     &input.IndexedBy,
		WorkflowID:    &input.WorkflowID,
		RunID:         &input.RunID,

		// Documents from indexing (MissingDocumentsList not stored here)
		Documents: docs,

		// Initialize with defaults
		NumberOfInstallments:   0,
		RevivalAmount:          0.0,
		InstallmentAmount:      0.0,
		TotalTaxOnUnpaid:       0.0,
		FirstCollectionDone:    false,
		BlockingNewCollections: true,
		InstallmentsPaid:       0,
	}

	// Create revival request in database (idempotent with ON CONFLICT)
	created, err := a.revivalRepo.CreateRevivalRequest(ctx, revivalReq)
	if err != nil {
		return "", fmt.Errorf("failed to create revival request: %w", err)
	}

	// Handle pointer return
	if created == nil {
		return "", fmt.Errorf("created revival request is nil")
	}

	return created.RequestID, nil
}

