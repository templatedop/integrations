// ProcessInstallmentActivity processes subsequent installment payment
func (a *Activities) ProcessInstallmentActivity(ctx context.Context, requestID string, installmentNumber int, amount float64, paymentMode, status string, collectionDate time.Time) error {
	log.Info(nil, "🎯 ProcessInstallmentActivity STARTED",
		"request_id", requestID,
		"installment_number", installmentNumber,
		"amount", amount,
		"payment_mode", paymentMode,
		"status", status,
		"collection_date", collectionDate)

	// Get revival request
	revivalReq, err := a.revivalRepo.GetRevivalRequestByID(ctx, requestID)
	if err != nil {
		log.Error(nil, "❌ Failed to get revival request in activity", "error", err)
		return fmt.Errorf("failed to get revival request: %w", err)
	}

	// Validate installment number
	if installmentNumber < 2 || installmentNumber > revivalReq.NumberOfInstallments {
		return fmt.Errorf("invalid installment number: %d, total installments: %d",
			installmentNumber, revivalReq.NumberOfInstallments)
	}

	// Create installment schedule record
	scheduleID := fmt.Sprintf("SCHED%s", time.Now().Format("20060102150405"))

	// Calculate due date (IR_11: 1st of each month)
	// This would be calculated based on installment number
	now := time.Now()
	dueDate := time.Date(now.Year(), now.Month()+time.Month(installmentNumber-1), 1, 0, 0, 0, now.Nanosecond(), now.Location())

	// Create installment schedule record in database
	isPaid := status == "PAID" || status == "CLEARED"
	err = a.revivalRepo.CreateInstallmentSchedule(
		ctx, scheduleID, requestID, revivalReq.PolicyNumber,
		installmentNumber, amount, 0, amount, dueDate, isPaid,
	)
	if err != nil {
		return fmt.Errorf("failed to create installment schedule: %w", err)
	}

	// Update revival request if payment is complete
	if status == "PAID" || status == "CLEARED" {
		log.Info(nil, "💰 Processing PAID installment", "installment_number", installmentNumber)

		// Increment installmentsPaid in revival request
		log.Info(nil, "⬆️ Incrementing installments_paid", "request_id", requestID)
		err = a.revivalRepo.IncrementInstallmentsPaid(ctx, requestID)
		if err != nil {
			log.Error(nil, "❌ Failed to increment installments paid", "error", err)
			return fmt.Errorf("failed to increment installments paid: %w", err)
		}
		log.Info(nil, "✅ Successfully incremented installments_paid")

		installmentPayment := domain.PaymentTransaction{
			RequestID:         requestID,
			PolicyNumber:      revivalReq.PolicyNumber,
			InstallmentNumber: &installmentNumber,
			Amount:            amount,
			TaxAmount:         0,
			TotalAmount:       amount,
			PaymentMode:       paymentMode,
			PaymentStatus:     "COMPLETED",
			CollectionDate:    collectionDate,
			CollectedBy:       &[]string{""}[0], // TODO: set from auth context
		}

		log.Info(nil, "💳 Creating payment record in database",
			"request_id", requestID,
			"installment_number", installmentNumber,
			"amount", amount,
			"collection_date", collectionDate)

		paymentID, err := a.paymentRepo.CreateInstallmentPayment(ctx, installmentPayment)
		if err != nil {
			log.Error(nil, "❌ Failed to create payment record", "error", err)
			return fmt.Errorf("failed to create installment payment transaction: %w", err)
		}

		log.Info(nil, "✅ Payment record created successfully", "payment_id", paymentID)

		// Check if all installments paid, set status to COMPLETED
		revivalReq, err := a.revivalRepo.GetRevivalRequestByID(ctx, requestID)
		if err != nil {
			return fmt.Errorf("failed to get revival request: %w", err)
		}

		totalPaid := float64(revivalReq.InstallmentsPaid) * revivalReq.InstallmentAmount

		completed := false

		if revivalReq.AdjustedRevivalAmount == 0 {
			if revivalReq.InstallmentsPaid >= revivalReq.NumberOfInstallments {
				completed = true
			}
		} else {
			// Re-revival: amount-based completion
			if totalPaid >= revivalReq.AdjustedRevivalAmount {
				completed = true
			}
		}

		if completed {
			// All installments paid - mark as COMPLETED
			err = a.revivalRepo.UpdateRevivalRequestForCompletion(ctx, requestID)
			if err != nil {
				return fmt.Errorf("failed to complete revival request: %w", err)
			}

			// Update policy status to IF (In Force)
			err = a.policyRepo.UpdatePolicyStatus(ctx, revivalReq.PolicyNumber, "IF")
			if err != nil {
				return fmt.Errorf("failed to update policy status: %w", err)
			}

			// Increment revival count
			now := time.Now()
			err = a.policyRepo.IncrementRevivalCount(ctx, revivalReq.PolicyNumber, &now)
			if err != nil {
				return fmt.Errorf("failed to increment revival count: %w", err)
			}
		}
	}

	return nil
}

// HandleDefaultActivity handles installment default (moves to suspense, reverts to AL, terminates workflow)
// This is called when an installment due date passes without payment (IR_9: Zero grace period)
// Process:
// 1. Create suspense entry for premium (first collection)
// 2. Create suspense entry for each paid installment
// 3. Create termination record in database
// 4. Revert policy status to AL (lapsed)
// 5. Update revival request status to DEFAULTED
func (a *Activities) HandleDefaultActivity(ctx context.Context, requestID string, installmentNumber int) error {
	// Get revival request
	revivalReq, err := a.revivalRepo.GetRevivalRequestByID(ctx, requestID)
	if err != nil {
		return fmt.Errorf("failed to get revival request: %w", err)
	}

	collectedPremium, err := a.paymentRepo.GetTotalPremiumPaid(ctx, requestID)
	if err != nil {
		return fmt.Errorf("failed to fetch collected premium: %w", err)
	}

	systemUser := "SYSTEM"
	totalSuspenseAmount := 0.0

	// 1. Move all collected amounts to suspense (IR_28)
	// Create suspense entry for premium amount (first collection)
	premiumSuspenseID := uuid.New().String()
	premiumSuspense := domain.SuspenseAccount{
		SuspenseID:          premiumSuspenseID,
		PolicyNumber:        revivalReq.PolicyNumber,
		RequestID:           &requestID,
		SuspenseType:        "REVIVAL_PREMIUM",
		SuspenseAccountType: "REVIVAL_SUSPENSE",
		Amount:              collectedPremium, // Premium amount from first collection
		IsReversed:          false,
		CreatedAt:           time.Now(),
		CreatedBy:           &systemUser,
		UpdatedAt:           time.Now(),
		Reason:              "Payment not made in due time",
	}

	err = a.paymentRepo.CreateSuspenseEntry(ctx, premiumSuspense)
	if err != nil {
		return fmt.Errorf("failed to create premium suspense entry: %w", err)
	}
	totalSuspenseAmount += revivalReq.RevivalAmount

	// 2. Create suspense entry for each collected installment (including first installment from dual collection)
	// First installment was part of first collection, subsequent paid are installments 2+
	installmentsPaidCount := revivalReq.InstallmentsPaid
	for i := 1; i <= installmentsPaidCount; i++ {
		installmentSuspenseID := uuid.New().String()
		installmentSuspense := domain.SuspenseAccount{
			SuspenseID:   installmentSuspenseID,
			PolicyNumber: revivalReq.PolicyNumber,
			RequestID:    &requestID,
			//SuspenseType: fmt.Sprintf("REVIVAL_INSTALLMENT_%d", i),
			SuspenseType:        "REVIVAL_INSTALLMENT",
			SuspenseAccountType: "REVIVAL_SUSPENSE",
			Amount:              revivalReq.InstallmentAmount,
			IsReversed:          false,
			CreatedAt:           time.Now(),
			CreatedBy:           &systemUser,
			UpdatedAt:           time.Now(),
		}

		err = a.paymentRepo.CreateSuspenseEntry(ctx, installmentSuspense)
		if err != nil {
			return fmt.Errorf("failed to create installment %d suspense entry: %w", i, err)
		}
		totalSuspenseAmount += revivalReq.InstallmentAmount
	}

	// 3. Create termination record
	terminationID := uuid.New().String()
	termination := domain.RevivalTermination{
		TerminationID:     terminationID,
		RequestID:         requestID,
		TicketID:          revivalReq.TicketID,
		PolicyNumber:      revivalReq.PolicyNumber,
		TerminationReason: fmt.Sprintf("Installment %d payment not received by due date (IR_9: Zero grace period)", installmentNumber),
		TerminationType:   "DEFAULT",
		InstallmentNumber: installmentNumber,
		SuspenseCreated:   true,
		SuspenseAmount:    totalSuspenseAmount,
		TerminatedAt:      time.Now(),
		TerminatedBy:      &systemUser,
		CreatedAt:         time.Now(),
	}

	err = a.revivalRepo.CreateRevivalTermination(ctx, &termination)
	if err != nil {
		return fmt.Errorf("failed to create revival termination record: %w", err)
	}

	// 4. Revert policy status to AL (lapsed)
	err = a.policyRepo.UpdatePolicyStatus(ctx, revivalReq.PolicyNumber, "AL")
	if err != nil {
		return fmt.Errorf("failed to revert policy status to AL: %w", err)
	}

	// 5. Update revival request status to DEFAULTED
	err = a.revivalRepo.UpdateRevivalRequestStatus(ctx, requestID, "DEFAULTED", "SYSTEM")
	if err != nil {
		return fmt.Errorf("failed to update revival request status to DEFAULTED: %w", err)
	}

	return nil
}

