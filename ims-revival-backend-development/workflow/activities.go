// Updates status to RETURNED_TO_INDEXER for indexer inbox
func (a *Activities) TerminateAndReturnToIndexerActivity(
	ctx context.Context,
	requestID string,
	reason string,
	stage string,
) error {
	// Update revival request status to RETURNED_TO_INDEXER
	err := a.revivalRepo.UpdateRevivalRequestStatus(
		ctx,
		requestID,
		"RETURNED_TO_INDEXER",
		"SYSTEM",
	)
	if err != nil {
		return fmt.Errorf("failed to update status: %w", err)
	}

	// Note: In production, would create indexer inbox entry
	// and send notification to indexer
	// For now, logging the termination
	_ = reason
	_ = stage

	return nil
}

// SuspenseAdjustmentResult contains the result of suspense adjustment check
type SuspenseAdjustmentResult struct {
	HasSuspense           bool    `json:"has_suspense"`
	TotalSuspenseAmount   float64 `json:"total_suspense_amount"`
	OriginalRevivalAmount float64 `json:"original_revival_amount"`
	AdjustedRevivalAmount float64 `json:"adjusted_revival_amount"`
	SuspenseEntriesCount  int     `json:"suspense_entries_count"`
}

// CheckAndAdjustSuspenseActivity checks for existing suspense and adjusts revival amount
// This is called during data entry for re-revival scenarios
// Process:
// 1. Check if policy has any non-reversed suspense entries
// 2. If suspense exists, calculate total suspense amount
// 3. Adjust revival amount = original_revival_amount - total_suspense
// 4. Mark all suspense entries as reversed (adjusted against new revival)
// 5. Update revival request with adjustment information
// 6. Return adjustment result for workflow decision-making
func (a *Activities) CheckAndAdjustSuspenseActivity(ctx context.Context, requestID, policyNumber string, originalRevivalAmount float64) (SuspenseAdjustmentResult, error) {
	result := SuspenseAdjustmentResult{
		HasSuspense:           false,
		TotalSuspenseAmount:   0.0,
		OriginalRevivalAmount: originalRevivalAmount,
		AdjustedRevivalAmount: originalRevivalAmount,
		SuspenseEntriesCount:  0,
	}

	// 1. Check for active suspense entries
	suspenseEntries, err := a.paymentRepo.GetActiveSuspenseByPolicy(ctx, policyNumber)
	if err != nil {
		log.Error(ctx, "Error fetching active suspense entries", err)
		return result, fmt.Errorf("failed to get active suspense: %w", err)
	}

	// If no suspense exists, return early with no adjustment
	if len(suspenseEntries) == 0 {
		return result, nil
	}

	// 2. Calculate total suspense amount
	totalSuspense := 0.0
	for _, entry := range suspenseEntries {
		totalSuspense += entry.Amount
	}

	// 3. Calculate adjusted revival amount
	adjustedAmount := originalRevivalAmount - totalSuspense

	fmt.Println("original amount:", originalRevivalAmount, "total suspense:", totalSuspense, "adjusted amount:", adjustedAmount)

	// Validate adjusted amount is positive
	if adjustedAmount < 0 {
		return result, fmt.Errorf(
			"suspense amount (%.2f) exceeds revival amount (%.2f), adjusted amount would be negative (%.2f)",
			totalSuspense, originalRevivalAmount, adjustedAmount,
		)
	}

	// 4. Mark suspense entries as reversed (adjusted against new revival)
	err = a.paymentRepo.ReverseSuspenseForAdjustment(ctx, policyNumber, requestID, "SYSTEM")
	if err != nil {
		return result, fmt.Errorf("failed to reverse suspense entries: %w", err)
	}

	// 5. Update revival request with adjustment information
	err = a.revivalRepo.UpdateRevivalRequestWithSuspenseAdjustment(ctx, requestID, totalSuspense, adjustedAmount)
	if err != nil {
		return result, fmt.Errorf("failed to update revival request with suspense adjustment: %w", err)
	}

	// 6. Return adjustment result
	result.HasSuspense = true
	result.TotalSuspenseAmount = totalSuspense
	result.AdjustedRevivalAmount = adjustedAmount
	result.SuspenseEntriesCount = len(suspenseEntries)

	return result, nil
}

// When all installments paid, set status to COMPLETED
func (a *Activities) FinalizeRevivalAfterFirstCollection(ctx context.Context, requestID string) error {
	// Get revival request
	revivalReq, err := a.revivalRepo.GetRevivalRequestByID(ctx, requestID)
	if err != nil {
		return fmt.Errorf("failed to get revival request: %w", err)
	}

	// 1. Mark revival COMPLETED
	err = a.revivalRepo.UpdateRevivalRequestForCompletion(ctx, requestID)
	if err != nil {
		return fmt.Errorf("failed to complete revival request: %w", err)
	}

	// 2. Update policy status to IF
	err = a.policyRepo.UpdatePolicyStatus(ctx, revivalReq.PolicyNumber, "IF")
	if err != nil {
		return fmt.Errorf("failed to update policy status: %w", err)
	}

	// 3. Increment revival count
	now := time.Now()
	err = a.policyRepo.IncrementRevivalCount(ctx, revivalReq.PolicyNumber, &now)
	if err != nil {
		return fmt.Errorf("failed to increment revival count: %w", err)
	}

	return nil
}

func (a *Activities) UpdateWorkflowStateActivity(
	ctx context.Context,
	requestID string,
	status string,
	slaStart, slaEnd time.Time,
) error {
	return a.revivalRepo.UpdateWorkflowState(ctx, requestID, status, slaStart, slaEnd)
}
