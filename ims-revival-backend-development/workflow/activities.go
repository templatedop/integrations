// PolicyValidationResult returned by ValidatePolicyActivity
// Contains maturity date to cache in workflow state (reduces DB hits for IR_4 validation)
type PolicyValidationResult struct {
	MaturityDate time.Time `json:"maturity_date"`
}

// ValidatePolicyActivity validates policy for revival and returns maturity date
// OPTIMIZED: Single DB query (batch) for policy + config + ongoing revival count
// Returns maturity date to cache in workflow state for IR_4 validation
func (a *Activities) ValidatePolicyActivity(ctx context.Context, policyNumber string) (PolicyValidationResult, error) {
	// Single batched query: GetPolicyByNumber + CheckOngoingRevival + GetMaxRevivalsAllowed
	// Reduces 3 DB round trips to 1
	validation, err := a.policyRepo.ValidatePolicyForRevival(ctx, policyNumber)
	if err != nil {
		return PolicyValidationResult{}, fmt.Errorf("policy validation failed: %w", err)
	}

	// Check if policy is in AL (lapsed) status
	if validation.Policy.PolicyStatus != "AL" {
		return PolicyValidationResult{}, fmt.Errorf("policy is not in lapsed status, current status: %s", validation.Policy.PolicyStatus)
	}

	// Check for ongoing revival requests
	if validation.OngoingRevivalCount > 0 {
		return PolicyValidationResult{}, fmt.Errorf("ongoing revival request exists for policy")
	}

	// Check max revivals allowed
	if validation.Policy.RevivalCount >= validation.MaxRevivalsAllowed {
		return PolicyValidationResult{}, fmt.Errorf("max revivals (%d) exceeded for policy", validation.MaxRevivalsAllowed)
	}

	// Return maturity date to cache in workflow state
	return PolicyValidationResult{
		MaturityDate: validation.Policy.MaturityDate,
	}, nil
}

// ValidateDualCollectionActivity validates dual collection request (IR_36)
func (a *Activities) ValidateDualCollectionActivity(ctx context.Context, input FirstCollectionInput) error {
	// Validate Premium + Installment + GST = Total Amount (IR_36)
	totalAmount := input.PremiumAmount + input.InstallmentAmount + input.InstallmentCGST + input.InstallmentSGST + input.PremiumCGST + input.PremiumSGST

	// Use tolerance-based comparison for floating point (0.01 tolerance for currency)
	tolerance := 0.01
	diff := totalAmount - input.TotalAmount
	if diff < -tolerance || diff > tolerance {
		return fmt.Errorf("amount mismatch: premium (%.2f) + installment (%.2f) + GST (%.2f) = %.2f, expected %.2f (diff: %.4f)",
			input.PremiumAmount, input.InstallmentAmount,
			input.PremiumSGST+input.PremiumCGST+input.InstallmentSGST+input.InstallmentCGST,
			totalAmount, input.TotalAmount, diff)
	}

	// Validate payment mode is valid
	validModes := map[string]bool{"CASH": true, "CHEQUE": true, "NEFT": true, "RTGS": true, "UPI": true, "CARD": true}
	if !validModes[input.PaymentMode] {
		return fmt.Errorf("invalid payment mode: %s", input.PaymentMode)
	}

	// Validate collection date is not in future
	if input.ChequeDate != nil && input.ChequeDate.After(time.Now()) {
		return fmt.Errorf("cheque date cannot be in future")
	}

	return nil
}

// ProcessDualPaymentActivity processes dual payment (premium + installment)
func (a *Activities) ProcessDualPaymentActivity(ctx context.Context, input FirstCollectionInput, status string) error {
	// Generate batch ID as UUID for database compatibility
	batchID := uuid.New().String()

	// Map status to enum values: PENDING, COMPLETED, FAILED, CANCELLED
	dbStatus := "PENDING"
	if status == "PAID" || status == "CLEARED" || status == "COMPLETED" {
		dbStatus = "COMPLETED"
	} else if status == "FAILED" {
		dbStatus = "FAILED"
	} else if status == "CANCELLED" {
		dbStatus = "CANCELLED"
	}

	// Create premium payment transaction
	premiumTaxAmount := input.PremiumSGST + input.PremiumCGST
	premiumPayment := domain.PaymentTransaction{
		RequestID:      input.RequestID,
		PolicyNumber:   input.PolicyNumber,
		CollectionDate: time.Now(),
		PaymentMode:    input.PaymentMode,
		Amount:         input.PremiumAmount,
		TaxAmount:      premiumTaxAmount,
		TotalAmount:    input.PremiumAmount + premiumTaxAmount,
		PaymentStatus:  dbStatus,
		CollectedBy:    &[]string{""}[0], // Will be set from auth context
	}

	// Create installment payment transaction
	installmentTaxAmount := input.InstallmentSGST + input.InstallmentCGST
	installmentPayment := domain.PaymentTransaction{
		RequestID:      input.RequestID,
		PolicyNumber:   input.PolicyNumber,
		CollectionDate: time.Now(),
		PaymentMode:    input.PaymentMode,
		Amount:         input.InstallmentAmount,
		TaxAmount:      installmentTaxAmount,
		TotalAmount:    input.InstallmentAmount + installmentTaxAmount,
		PaymentStatus:  dbStatus,
		CollectedBy:    &[]string{""}[0], // Will be set from auth context
	}

	// Create dual payment records in batch (IR_36)
	err := a.paymentRepo.CreateDualPaymentRecords(ctx, premiumPayment, installmentPayment, batchID)
	if err != nil {
		return fmt.Errorf("failed to create dual payment records: %w", err)
	}

	// Update revival request status if payment is complete
	if status == "PAID" || status == "CLEARED" {
		err = a.revivalRepo.UpdateRevivalRequestForFirstCollection(ctx, input.RequestID)
		if err != nil {
			return fmt.Errorf("failed to update revival request for first collection: %w", err)
		}
	}

	return nil
}

